export function t(key: string, vars?: Record<string, string | number>): string {
  const entry = translations[currentLang]?.[key] ?? key;
  if (!vars) return entry;
  return entry.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? ''));
}
